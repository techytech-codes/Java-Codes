public class DoWhileDemo {

	public static void main(String[] args) {
		int i=1;
		do {
			System.out.println(i);
			i++;
		}while(i<=5);
		
		System.out.println("while demo......");
		int j=1;
		while(j<=5) {
			System.out.println(j);
			j++;
		}
		System.out.println("for loop.....");
		
		for(int k=1;k<=5;k++) {
			System.out.println(k);
		}		
	}

}